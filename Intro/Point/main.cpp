double A_length_B(Point A, Point B)
{
	double Len;
	Len = sqrt((A.get_x() - B.get_x()) * (A.get_x() - B.get_x()) + (A.get_y() - B.get_y()) * (A.get_y() - B.get_y()));
	return Len;
}